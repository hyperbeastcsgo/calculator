<?php
    namespace hyperbeastcsgo\calculater;
    class Cals 
    {
        public function add($a,$b)
        {
                return $a+$b;
        }
        public function del($a,$b)
        {
                return $a-$b;
        }
        public function mutiply($a,$b)
        {
                return $a*$b;
        }
        public function sub($a,$b)
        {
                return $a/$b;
        }

    }
?>